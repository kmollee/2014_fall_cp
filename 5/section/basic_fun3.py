def greet(lang):
    if lang == 'ch':
        return "你好"
    elif lang == "en":
        return "Hello"
    elif lang == "fr":
        return "Bonjour"
    else:
        return "@#$%^&*"
print(greet("ch"), "Lin")
print(greet("en"), "Sally")
print(greet("fr"), "Glenn")

